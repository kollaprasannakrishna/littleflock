            <h3 class="post-title">Our History</h3>
        </header>
        <div class="post-content">
            <div class="row">
                <div class="col-md-6">
                    <p>It was in the Fall of 1987 that a brother, in obedience to the call of the Lord, started this work together with his wife and their three children. The work began in a small way in their home in East Lansing, Michigan.
                        It started as a Children’s ministry and grew to be a Bible Study group and then into a Church, named later as Little Flock Christian Fellowship.
                        The name was prompted by a letter from a brother in India who quoted Luke 12:32 to encourage the group.
                        Those who are familiar with the ministry of Brother Watchman Nee (1903-1972) whom God used mightily in China, might wonder if we are connected to the Little Flock Assemblies he had started in China.
                        We greatly appreciate Brother Nee’s ministry but have no direct relation to his work. The brother and his wife whom God used to start this ministry along with their children, had several years of close fellowship with Brother Bakht Singh (1903-2000) and his ministry in India.
                        God used Bakht Singh, both in India and in many other countries for salvation of souls and establishment of a few thousands of churches which, as many have observed, are very close to the early church in terms of their understanding and following the teachings of the Apostles.</p>
                    <p> For a few years, the Little Flock met in the home of the family that God used as an instrument to start this ministry.
                        In the days -</p>
                </div>
                <div class="col-md-6">
                    <p> of the early Apostles there were many house churches (Col. 4:15; Rom. 16:5; Phil. 1:2).
                        Even today there are many house churches all over the world. As the number of believers increased, in the year 1995 the Little Flock’s Sunday services were moved to the conference hall of a hotel in East Lansing.
                        After the hotel was closed due to a serious fire accident in 2004, the services were held in the community halls of the Michigan State University.
                        By God’s grace we now have the Michigan State University Alumni Chapel for our Sunday services.
                        Weekly Bible studies and prayer meetings are held in the home of the pastor.As time passed, some of the fellow believers moved to other cities, where we began to visit them for the purpose of praying and studying the Word with them.
                        In 1997, the Lord led us to start monthly Bible Studies in the city of Farmington Hills, near Detroit.
                        The grace of God was upon this ministry. The Lord blessed these meetings for the edification of many believers in that area.
                        After waiting on the Lord for more than one year and believing that it was God’s will, on June 6, 1999 –The Lord’s Day – the believers of that area gathered together for their first worship service which included ‘breaking of bread’.
                        That was the beginning of the church in the Detroit area. We now meet in a hall rented from the Presbyterian church of Farmington Hills.
                        These days, believers from different cities in the Detroit area partake in these meetings. Praise the Lord!</p>
                </div>
            </div>
        </div>

        <div class="row">
            <div class="col-md-12">
               <p>For about three years starting in 1999, some of us periodically visited a sister who had moved to Mississauga, Ontario, Canada.
                   In the year 2000, a few more believers moved into this area from India, and they began to call us to visit them and to hold prayer
                   meetings and Bible studies for them, as and when possible. In response to this Macedonian call (Acts 16:9), the Lord enabled us to
                   visit them once a month. As we  continued those meetings, the Lord gave us a growing burden for this area and we began to pray
                   earnestly seeking the Lord’s guidance for further ministry over there.</p>
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <p>On February 21, 2003, we had an all-night prayer meeting in Brampton, Ontario, Canada. This was attended by several believers both
                    from the Michigan and Toronto regions. The next day, being Saturday, some of the believers continued to wait on the Lord seeking His
                    guidance.  Believing that it was God’s will, on February 23 – The Lord’s Day we had our first worship service at Brampton, a city
                    near Toronto, Ontario.  Presently, the believers here are immigrants from other countries, which reminds us of the church at Antioch
                    (Acts 11:19-24). They have taken the name Antioch Christian Fellowship for their assembly. They presently meet for worship services
                    on Sundays in a conference hall at the Marriott Hotel, Mississauga, and for prayer at homes of believers.</p>
            </div>
            <div class="col-md-6">
                <p>Thus, we were led step by step by the skillfulness of His loving hands to move on with the work of the Lord.
                    We had no plans of our own for these areas. It has always been our desire that we should not do God’s work in our own way or for
                    our own satisfaction. We desire to be led by the Lord.
                    Little Flock has not been free from attacks of the enemy (Eph. 6:12). Apostle Paul says: A great and effectual door is opened for
                    me, and there are many adversaries (I Cor. 16:9). The enemy at various times tried to disturb the work by his subtle means, but
                    the Lord gave us victory. We are aware that we are called to be His coworkers and laborers together with Him
                    (I Cor. 3:9; II Cor. 6:1); it is the Lord who is building His church (Matt. 16: 18). The battle is the Lord’s (II Chro. 20:15).</p>
