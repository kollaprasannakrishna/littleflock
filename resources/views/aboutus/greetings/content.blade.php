        <h3 class="post-title">Greetings to you in the name of our Lord and Savior Jesus Christ!</h3>
    </header>
    <div class="post-content">
        <div class="row">
          <div class="col-md-6">
              <div class="grid-item-inner"> <a href="http://placehold.it/600x400&amp;text=IMAGE+PLACEHOLDER" data-rel="prettyPhoto" class="media-box"> <img src="http://placehold.it/600x400&amp;text=IMAGE+PLACEHOLDER" alt=""> </a> </div>
          </div>
            <div class="col-md-6">
                <p><span class="label label-danger">Hard to believe</span>– we are already about seven years into the 21st century and rolling fast in the new millennium. Time is one thing that is constantly moving
                    ahead whether we notice it or not. We, who look forward for the coming of the Lord Jesus Christ should rejoice in the fact that we are moving towards that great
                    and wonderful day when – The dead in Christ shall rise first: then we which are alive and remain shall be caught up together with them in the clouds, to meet
                    the Lord in the air: and so shall we ever be with the Lord – I Thessalonians 4:16-17.</p>

            </div>
        </div>
        <div class="row" style="margin-top: 10px">
            <div class="col-md-12">
                <p>In the recent years the world has witnessed many horrors resulting from great catastrophes like famines, deadly diseases, tsunamis, hurricanes, and
                    earthquakes. Hundreds of thousands of people lost their lives. Wars and rumors of wars are in the daily news. In such a world asthis, the Lord has entrusted us
                    with His precious gospel. The world needs the gospel urgently as, every day thousands of people are going into eternal damnation. One can go to heaven
                    without a house, bank balance, car or good health, but cannot go there without Christ. As such, the world needs the gospel of the Lord Jesus Christ more than
                    anything else.</p>

            </div>

        </div>
        <div class="row">
            <div class="col-md-6">
                <p>Paul said: Woe is unto me, if I preach not the gospel (I Cor. 9:16). He told Timothy: Do the work of an evangelist (2 Tim. 4:5). Let us wake up to this call! God’s
                    people are small in number in this world, but they are told not to fear. The Lord says, “Fear not little flock, it is your father’s good pleasure to give you the
                    kingdom” (Luke 12:32). Fear is natural and common to all humans. But, when fear dominates our minds and hearts, we run into serious problems; even our faith
                    can be shaken. That’s why the devil tries his best to create fear in us through various means. </p>
            </div>
            <div class="col-md-6">
                <p>  But, we have to anchor our faith in the Lord (Hebrews 6:19). The
                    Lord wants His people to be together in fellowship (Ps. 50:5; Matthew 18:20; Acts 2:42). By being together we can strengthen one another in Christ. I would like
                    to encourage you to continue to be in fellowship with believers.
                    If you are looking for Christian fellowship in Lansing or Detroit, or Toronto (Canada) area, you are welcome to join us. The Word of God – The Holy Bible – is the
                    basis and guide for all our ministries. Our desire is to follow Christ and serve him faithfully.</p>
            </div>
